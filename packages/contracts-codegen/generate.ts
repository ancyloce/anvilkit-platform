// 3. Artifact manifest: validate fixtures, generate artifact package.
// ---------------------------------------------------------------------------

function generateArtifact(): void {
  const schemaPath = join(ARTIFACT_DIR, "artifact-manifest.schema.json");
  const raw = readFileSync(schemaPath, "utf8");
  if (raw.includes("`")) throw new Error("artifact-manifest.schema.json contains a backtick");
  const schema = readJson(schemaPath) as Schema;
  const props = (schema.properties ?? {}) as { [k: string]: Schema };
  const noRefs = () => {
    throw new Error("$ref not supported in the artifact schema");
  };

  // Validate fixtures.
  const fixturePath = join(ARTIFACT_DIR, "fixtures", "artifact-manifest.json");
  assertValid(schema, readJson(fixturePath), schema, "artifact-manifest.json");
  const invalidDir = join(ARTIFACT_DIR, "fixtures", "invalid");
  for (const name of readdirSync(invalidDir).sort()) {
    assertInvalid(schema, readJson(join(invalidDir, name)), schema, name);
  }

  let out = GENERATED_HEADER("contracts/artifact/v1");
  out += "\n";
  out +=
    "// Package artifact contains Go bindings for artifact-manifest.json —\n" +
    "// the canonical description of one deployment's static artifact bundle\n" +
    "// (PRD 0008 §9.2; FR-012). The manifest is internal-only (private,\n" +
    "// max-age=0, no-store) and routes[] is always an array. The JSON Schema\n" +
    "// file is the contract of record; this package is generated from it.\n";
  out += "package artifact\n\n";
  out += emitStruct(
    "ManifestFile",
    "is one object in the artifact bundle. contentHash matches the object's x-amz-meta-content-sha256 metadata — never an S3 ETag (FR-011).",
    props.files.items as Schema,
    {},
    noRefs,
  );
  out += "\n";
  out += emitStruct(
    "ManifestRoute",
    "is one route mapping within the artifact.",
    props.routes.items as Schema,
    {},
    noRefs,
  );
  out += "\n";
  out += emitStruct(
    "Manifest",
    "is artifact-manifest.json (schemaVersion 1).",
    schema,
    {},
    noRefs,
    {},
    { files: "[]ManifestFile", routes: "[]ManifestRoute" },
  );
  writeFile(join(GO_CONTRACTS_DIR, "artifact", "manifest_gen.go"), out);

  let schemaOut = GENERATED_HEADER("contracts/artifact/v1");
  schemaOut += "\npackage artifact\n\n";
  schemaOut +=
    "// SchemaArtifactManifest is the raw JSON Schema source (draft 2020-12),\n" +
    "// embedded so the manifest builder can validate its own output offline\n" +
    "// (EW-STORAGE-005 DoD).\n";
  schemaOut += `const SchemaArtifactManifest = \`${raw}\`\n`;
  writeFile(join(GO_CONTRACTS_DIR, "artifact", "schemas_gen.go"), schemaOut);

  copyFixture(fixturePath, join(GO_CONTRACTS_DIR, "artifact", "testdata", "artifact-manifest.json"));
}

