function generateEvents(): void {
  const enums = new Map<string, EnumSpec>();
  const structs: string[] = [];
  const schemaConsts: { constName: string; raw: string }[] = [];

  for (const cfg of EVENT_CONFIG) {
    const schemaPath = join(EVENTS_DIR, cfg.file);
    const raw = readFileSync(schemaPath, "utf8");
    const schema = readJson(schemaPath) as Schema;
    const props = (schema.properties ?? {}) as { [k: string]: Schema };

    for (const [prop, propSchema] of Object.entries(props)) {
      const goType = EVENT_ENUM_TYPES[prop];
      if (!goType || !Array.isArray(propSchema.enum)) continue;
      const values = propSchema.enum as string[];
      const existing = enums.get(goType);
      if (existing) {
        if (JSON.stringify(existing.values) !== JSON.stringify(values)) {
          throw new Error(`enum ${goType} differs between event schemas`);
        }
      } else {
        enums.set(goType, { goType, values, doc: EVENT_ENUM_DOCS[goType] });
      }
    }

    structs.push(
      emitStruct(cfg.goType, cfg.doc, schema, EVENT_ENUM_TYPES, () => {
        throw new Error("$ref not supported in event schemas");
      }),
    );
    if (raw.includes("`")) throw new Error(`${cfg.file} contains a backtick`);
    schemaConsts.push({ constName: `Schema${cfg.goType}`, raw });

    // Validate fixtures.
    const fixturePath = join(EVENTS_DIR, "fixtures", cfg.file.replace(".schema.json", ".json"));
    assertValid(schema, readJson(fixturePath), schema, relative(REPO_ROOT, fixturePath));
  }

  // Invalid fixtures must fail against their schema (longest-prefix filename match).
  const invalidDir = join(EVENTS_DIR, "fixtures", "invalid");
  for (const name of readdirSync(invalidDir).sort()) {
    const cfg = EVENT_CONFIG.filter((c) =>
      name.startsWith(c.file.replace(".schema.json", "")),
    ).sort((a, b) => b.file.length - a.file.length)[0];
    if (!cfg) throw new Error(`invalid fixture ${name} matches no event schema`);
    const schema = readJson(join(EVENTS_DIR, cfg.file)) as Schema;
    assertInvalid(schema, readJson(join(invalidDir, name)), schema, name);
  }

  const eventTypeConsts = EVENT_CONFIG.map((c) => {
    const schema = readJson(join(EVENTS_DIR, c.file)) as Schema;
    const props = schema.properties as { [k: string]: Schema };
    return { name: `EventType${c.goType}`, value: props.eventType.const as string };
  });

  let out = GENERATED_HEADER("contracts/events/v1");
  out += "\n";
  out +=
    "// Package events contains Go bindings for the versioned AnvilKit platform\n" +
    "// event contracts (contracts/events/v1). The JSON Schema files are the\n" +
    "// contract of record; this package is generated from them and committed so\n" +
    "// the worker repo builds standalone. Platform CI regenerates and fails on\n" +
    "// drift. Contracts evolve additively only (FR-002, ADR-001).\n";
  out += "package events\n\n";
  out += "// Event type discriminators.\nconst (\n";
  for (const c of eventTypeConsts) out += `\t${c.name} = "${c.value}"\n`;
  out += ")\n\n";
  for (const goType of ["RenderMode", "Environment", "ErrorClassification", "FailedStage", "ErrorCode"]) {
    const spec = enums.get(goType);
    if (spec) out += emitEnum(spec) + "\n";
  }
  out += structs.join("\n");
  writeFile(join(GO_CONTRACTS_DIR, "events", "events_gen.go"), out);

  let schemasOut = GENERATED_HEADER("contracts/events/v1");
  schemasOut += "\npackage events\n\n";
  schemasOut +=
    "// Raw JSON Schema sources (draft 2020-12), embedded so the worker can\n" +
    "// validate payloads offline without a filesystem dependency on the\n" +
    "// platform repo.\nconst (\n";
  for (const sc of schemaConsts) {
    schemasOut += `\t${sc.constName} = \`${sc.raw}\`\n\n`;
  }
  schemasOut += ")\n";
  writeFile(join(GO_CONTRACTS_DIR, "events", "schemas_gen.go"), schemasOut);

  for (const cfg of EVENT_CONFIG) {
    const fixture = cfg.file.replace(".schema.json", ".json");
    copyFixture(
      join(EVENTS_DIR, "fixtures", fixture),
      join(GO_CONTRACTS_DIR, "events", "testdata", fixture),
    );
  }
}

