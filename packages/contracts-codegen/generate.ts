function walkContracts(dir: string, acc: string[]): void {
  for (const name of readdirSync(dir).sort()) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) {
      walkContracts(full, acc);
    } else {
      acc.push(full);
    }
  }
}

function computeLock(): { [k: string]: string } {
  const files: string[] = [];
  walkContracts(CONTRACTS_DIR, files);
  const lock: { [k: string]: string } = {};
  for (const f of files) {
    const rel = relative(REPO_ROOT, f);
    if (rel === "contracts/contracts.lock.json" || rel === "contracts/README.md") continue;
    lock[rel] = createHash("sha256").update(readFileSync(f)).digest("hex");
  }
  return lock;
}

