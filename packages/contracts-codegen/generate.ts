// ---------------------------------------------------------------------------
// Minimal JSON Schema (draft 2020-12 subset) validator.
// Supports exactly the keywords used by the contracts in this repository:
// type, properties, required, additionalProperties, enum, const, pattern,
// minLength, minimum, maximum, items, $ref (within one document).
// ---------------------------------------------------------------------------

type Json = null | boolean | number | string | Json[] | { [k: string]: Json };
type Schema = { [k: string]: Json };

function resolveRef(root: Json, ref: string): Schema {
  if (!ref.startsWith("#/")) throw new Error(`unsupported $ref: ${ref}`);
  let node: Json = root;
  for (const part of ref.slice(2).split("/")) {
    if (node === null || typeof node !== "object" || Array.isArray(node)) {
      throw new Error(`$ref not found: ${ref}`);
    }
    node = (node as { [k: string]: Json })[part];
    if (node === undefined) throw new Error(`$ref not found: ${ref}`);
  }
  return node as Schema;
}

function validate(
  schema: Schema,
  value: Json,
  root: Json,
  path: string,
  errors: string[],
): void {
  if (typeof schema.$ref === "string") {
    validate(resolveRef(root, schema.$ref), value, root, path, errors);
    return;
  }
  if ("const" in schema) {
    if (JSON.stringify(value) !== JSON.stringify(schema.const)) {
      errors.push(`${path}: expected const ${JSON.stringify(schema.const)}`);
    }
    return;
  }
  if (Array.isArray(schema.enum)) {
    if (!schema.enum.some((v) => JSON.stringify(v) === JSON.stringify(value))) {
      errors.push(`${path}: value ${JSON.stringify(value)} not in enum`);
      return;
    }
  }
  const type = schema.type;
  if (type === "object") {
    if (value === null || typeof value !== "object" || Array.isArray(value)) {
      errors.push(`${path}: expected object`);
      return;
    }
    const obj = value as { [k: string]: Json };
    const props = (schema.properties ?? {}) as { [k: string]: Schema };
    for (const req of (schema.required ?? []) as string[]) {
      if (!(req in obj)) errors.push(`${path}: missing required property ${req}`);
    }
    for (const [key, val] of Object.entries(obj)) {
      if (key in props) {
        validate(props[key], val, root, `${path}.${key}`, errors);
      } else if (schema.additionalProperties === false) {
        errors.push(`${path}: additional property ${key} not allowed`);
      }
    }
    return;
  }
  if (type === "array") {
    if (!Array.isArray(value)) {
      errors.push(`${path}: expected array`);
      return;
    }
    const items = schema.items as Schema | undefined;
    if (items) value.forEach((v, i) => validate(items, v, root, `${path}[${i}]`, errors));
    return;
  }
  if (type === "string") {
    if (typeof value !== "string") {
      errors.push(`${path}: expected string`);
      return;
    }
    if (typeof schema.minLength === "number" && value.length < schema.minLength) {
      errors.push(`${path}: shorter than minLength ${schema.minLength}`);
    }
    if (typeof schema.pattern === "string" && !new RegExp(schema.pattern).test(value)) {
      errors.push(`${path}: does not match pattern ${schema.pattern}`);
    }
    return;
  }
  if (type === "integer" || type === "number") {
    if (typeof value !== "number" || (type === "integer" && !Number.isInteger(value))) {
      errors.push(`${path}: expected ${type}`);
      return;
    }
    if (typeof schema.minimum === "number" && value < schema.minimum) {
      errors.push(`${path}: below minimum ${schema.minimum}`);
    }
    if (typeof schema.maximum === "number" && value > schema.maximum) {
      errors.push(`${path}: above maximum ${schema.maximum}`);
    }
    return;
  }
  if (type === "boolean") {
    if (typeof value !== "boolean") errors.push(`${path}: expected boolean`);
    return;
  }
}

function assertValid(schema: Schema, value: Json, root: Json, label: string): void {
  const errors: string[] = [];
  validate(schema, value, root, "$", errors);
  if (errors.length > 0) {
    throw new Error(`fixture ${label} failed validation:\n  ${errors.join("\n  ")}`);
  }
}

function assertInvalid(schema: Schema, value: Json, root: Json, label: string): void {
  const errors: string[] = [];
  validate(schema, value, root, "$", errors);
  if (errors.length === 0) {
    throw new Error(`invalid fixture ${label} unexpectedly passed validation`);
  }
}

// ---------------------------------------------------------------------------
// Go naming helpers.
// ---------------------------------------------------------------------------

const INITIALISMS = new Set(["id", "url", "uri", "api", "http", "json"]);

function goFieldName(prop: string): string {
  // camelCase property → exported Go field, honoring common initialisms.
  const words = prop
    .replace(/([a-z0-9])([A-Z])/g, "$1 $2")
    .split(/[\s_.-]+/)
    .filter(Boolean);
  return words
    .map((w) => {
      const lower = w.toLowerCase();
      if (INITIALISMS.has(lower)) return lower.toUpperCase();
      return w.charAt(0).toUpperCase() + w.slice(1);
    })
    .join("");
}

function goConstSuffix(value: string): string {
  // enum value → Go constant name fragment, e.g. RENDER_ORIGIN_5XX → RenderOrigin5xx.
  return value
    .split(/[^A-Za-z0-9]+/)
    .filter(Boolean)
    .map((tok) => {
      const lower = tok.toLowerCase();
      if (/^[0-9]/.test(lower)) return lower;
      return lower.charAt(0).toUpperCase() + lower.slice(1);
    })
    .join("");
}

function goArgName(param: string): string {
  const field = goFieldName(param);
  return field.charAt(0).toLowerCase() + field.slice(1);
}

// ---------------------------------------------------------------------------
// Go type emission from schema properties.
// ---------------------------------------------------------------------------

interface EnumSpec {
  goType: string;
  values: string[];
  doc: string;
}

function propGoType(
  prop: string,
  schema: Schema,
  enumTypes: Record<string, string>,
  refNamer: (ref: string) => string,
  typeOverrides: Record<string, string> = {},
): string {
  if (prop in typeOverrides) return typeOverrides[prop];
  if (typeof schema.$ref === "string") return refNamer(schema.$ref);
  if (prop in enumTypes && Array.isArray(schema.enum)) return enumTypes[prop];
  if ("const" in schema) {
    return typeof schema.const === "number" ? "int64" : "string";
  }
  switch (schema.type) {
    case "string":
      return "string";
    case "integer":
      return "int64";
    case "number":
      return "float64";
    case "boolean":
      return "bool";
    case "array": {
      const items = schema.items as Schema | undefined;
      if (items && typeof items.$ref === "string") return "[]" + refNamer(items.$ref);
      if (items && items.type === "string") return "[]string";
      throw new Error(`unsupported array items for property ${prop}`);
    }
    default:
      throw new Error(`unsupported schema type for property ${prop}: ${schema.type}`);
  }
}

function emitStruct(
  goName: string,
  doc: string,
  schema: Schema,
  enumTypes: Record<string, string>,
  refNamer: (ref: string) => string,
  fieldRenames: Record<string, string> = {},
  typeOverrides: Record<string, string> = {},
): string {
  const required = new Set((schema.required ?? []) as string[]);
  const props = (schema.properties ?? {}) as { [k: string]: Schema };
  let out = `// ${goName} ${doc}\n`;
  out += `type ${goName} struct {\n`;
  for (const [prop, propSchema] of Object.entries(props)) {
    const goType = propGoType(prop, propSchema, enumTypes, refNamer, typeOverrides);
    const omit = required.has(prop) ? "" : ",omitempty";
    const fieldName = fieldRenames[prop] ?? goFieldName(prop);
    const desc = typeof propSchema.description === "string" ? propSchema.description : "";
    if (desc) out += `\t// ${fieldName}: ${desc.replace(/\n/g, " ")}\n`;
    out += `\t${fieldName} ${goType} \`json:"${prop}${omit}"\`\n`;
  }
  out += "}\n";
  return out;
}

function emitEnum(spec: EnumSpec): string {
  let out = `// ${spec.goType} ${spec.doc}\n`;
  out += `type ${spec.goType} string\n\n`;
  out += `// ${spec.goType} values.\nconst (\n`;
  for (const v of spec.values) {
    out += `\t${spec.goType}${goConstSuffix(v)} ${spec.goType} = "${v}"\n`;
  }
  out += ")\n";
  return out;
}

