interface Operation {
  method: string;
  path: string;
  op: Schema;
}

function collectOperations(doc: Schema): Operation[] {
  const ops: Operation[] = [];
  const paths = doc.paths as { [k: string]: Schema };
  for (const [path, item] of Object.entries(paths)) {
    for (const method of ["get", "post", "patch", "put", "delete"]) {
      if (item[method]) ops.push({ method, path, op: item[method] as Schema });
    }
  }
  return ops;
}

function errorResponseSchemas(doc: Schema): Set<string> {
  // Component schema names referenced from non-2xx response bodies.
  const names = new Set<string>();
  for (const { op } of collectOperations(doc)) {
    const responses = (op.responses ?? {}) as { [k: string]: Schema };
    for (const [status, resp] of Object.entries(responses)) {
      if (status.startsWith("2")) continue;
      const ref = (resp as any)?.content?.["application/json"]?.schema?.$ref;
      if (typeof ref === "string") names.add(ref.split("/").pop() as string);
    }
  }
  return names;
}

function generateService(cfg: ServiceConfig): void {
  const docPath = join(OPENAPI_DIR, cfg.file);
  const doc = readJson(docPath) as Schema;
  const schemas = ((doc.components as Schema).schemas ?? {}) as { [k: string]: Schema };
  const refNamer = (ref: string) => ref.split("/").pop() as string;
  const errorSchemas = errorResponseSchemas(doc);
  const sourceRel = `contracts/openapi/v1/${cfg.file}`;

  // Validate fixtures against component schemas.
  for (const [fixture, schemaName] of Object.entries(cfg.fixtures)) {
    const fixturePath = join(OPENAPI_DIR, "fixtures", fixture);
    assertValid(schemas[schemaName], readJson(fixturePath), doc, fixture);
    copyFixture(fixturePath, join(GO_CONTRACTS_DIR, cfg.goPackage, "testdata", fixture));
  }

  // Invalid fixtures must fail against their component schema (parity with the
  // events/artifact corpora). Validation-only: not copied into Go testdata.
  for (const [fixture, schemaName] of Object.entries(cfg.invalidFixtures)) {
    const fixturePath = join(OPENAPI_DIR, "fixtures", "invalid", fixture);
    assertInvalid(schemas[schemaName], readJson(fixturePath), doc, fixture);
  }

  // --- types_gen.go ---
  let types = GENERATED_HEADER(sourceRel);
  types += "\n";
  types += `// Package ${cfg.goPackage} contains Go bindings for the ${(doc.info as Schema).title}.\n`;
  types += `// The OpenAPI document is the contract of record; shapes preserve\n`;
  types += `// PRD 0008 §8 / PRD 0010 §8 exactly. Generated and committed; platform CI\n`;
  types += `// regenerates and fails on drift (FR-002).\n`;
  types += `package ${cfg.goPackage}\n\n`;
  types += `import "fmt"\n\n`;
  const structs: string[] = [];
  const enumsOut: string[] = [];
  for (const [name, schema] of Object.entries(schemas)) {
    if (schema.type === "string" && Array.isArray(schema.enum)) {
      enumsOut.push(
        emitEnum({
          goType: name,
          values: schema.enum as string[],
          doc: typeof schema.description === "string" ? schema.description : "",
        }),
      );
      continue;
    }
    const doc_ = typeof schema.description === "string" ? schema.description : "";
    // Error-response schemas grow an Error() method — rename a property that
    // would collide with it (e.g. StatusConflictError.error → ErrorCode).
    const renames: Record<string, string> = errorSchemas.has(name) ? { error: "ErrorCode" } : {};
    structs.push(emitStruct(name, doc_, schema, {}, refNamer, renames));
  }
  types += enumsOut.concat(structs).join("\n");
  // Error() methods for schemas used as error response bodies.
  for (const name of [...errorSchemas].sort()) {
    types += `\n// Error implements the error interface: a decoded ${name} response is\n`;
    types += `// returned directly as a typed error by the generated client.\n`;
    types += `func (e *${name}) Error() string {\n`;
    types += `\treturn fmt.Sprintf("${cfg.goPackage}: %+v", *e)\n`;
    types += `}\n`;
  }
  if (!types.includes("fmt.Sprintf")) {
    types = types.replace(`import "fmt"\n\n`, "");
  }
  writeFile(join(GO_CONTRACTS_DIR, cfg.goPackage, "types_gen.go"), types);

  // --- client_gen.go ---
  const hasPathParams = collectOperations(doc).some(({ op }) =>
    ((op.parameters ?? []) as Schema[]).some((p) => {
      const resolved = typeof p.$ref === "string" ? resolveRef(doc, p.$ref) : p;
      return resolved.in === "path";
    }),
  );
  let cl = GENERATED_HEADER(sourceRel);
  cl += `\npackage ${cfg.goPackage}\n\n`;
  const imports = ["bytes", "context", "encoding/json", "fmt", "io", "net/http"];
  if (hasPathParams) imports.push("net/url");
  imports.push("strings");
  cl += `import (\n${imports.map((i) => `\t"${i}"`).join("\n")}\n)\n\n`;
  cl += `// Client is a minimal generated client for the ${(doc.info as Schema).title}.\n`;
  cl += `// It owns transport mechanics only. Worker-level concerns — retryable-error\n`;
  cl += `// mapping, timeout budgets, alerting classification — belong to the client\n`;
  cl += `// wrapper tasks (EW-DEPLOY-001 / EW-ARTIFACT-*), not to generated code.\n`;
  cl += `type Client struct {\n`;
  cl += `\t// BaseURL is the service base URL, e.g. ${((doc.servers as Json[])[0] as Schema).url}.\n`;
  cl += `\tBaseURL string\n`;
  cl += `\t// Token is the INTERNAL_SERVICE_TOKEN bearer credential (PRD 0010 §11.1).\n`;
  cl += `\tToken string\n`;
  cl += `\t// HTTPClient is the underlying transport; http.DefaultClient when nil.\n`;
  cl += `\tHTTPClient *http.Client\n`;
  cl += `}\n\n`;
  cl += `// APIError is returned for responses with no operation-specific mapping.\n`;
  cl += `type APIError struct {\n\tStatusCode int\n\tBody       []byte\n}\n\n`;
  cl += `func (e *APIError) Error() string {\n`;
  cl += `\tbody := e.Body\n\tif len(body) > 512 {\n\t\tbody = body[:512]\n\t}\n`;
  cl += `\treturn fmt.Sprintf("${cfg.goPackage}: unexpected status %d: %s", e.StatusCode, string(body))\n}\n\n`;

  for (const { method, path, op } of collectOperations(doc)) {
    const operationId = op.operationId as string;
    const goMethod = operationId.charAt(0).toUpperCase() + operationId.slice(1);
    const params = ((op.parameters ?? []) as Schema[]).map((p) =>
      typeof p.$ref === "string" ? resolveRef(doc, p.$ref) : p,
    );
    const pathParams = params.filter((p) => p.in === "path").map((p) => p.name as string);
    const bodyRef = (op as any)?.requestBody?.content?.["application/json"]?.schema?.$ref;
    const bodyType = typeof bodyRef === "string" ? refNamer(bodyRef) : null;
    const responses = (op.responses ?? {}) as { [k: string]: Schema };
    let successType: string | null = null;
    for (const [status, resp] of Object.entries(responses)) {
      if (!status.startsWith("2")) continue;
      const ref = (resp as any)?.content?.["application/json"]?.schema?.$ref;
      if (typeof ref === "string") successType = refNamer(ref);
    }
    const errorHooks: { status: string; type: string }[] = [];
    for (const [status, resp] of Object.entries(responses)) {
      if (status.startsWith("2")) continue;
      const ref = (resp as any)?.content?.["application/json"]?.schema?.$ref;
      if (typeof ref === "string") errorHooks.push({ status, type: refNamer(ref) });
    }

    // Build Go path expression from the template.
    const segments = path.split("/").filter(Boolean);
    const exprParts: string[] = [];
    let literal = "";
    for (const seg of segments) {
      const m = seg.match(/^\{(.+)\}$/);
      if (m) {
        if (literal) {
          exprParts.push(`"${literal}/"`);
          literal = "";
        } else {
          exprParts.push(`"/"`);
        }
        exprParts.push(`url.PathEscape(${goArgName(m[1])})`);
      } else {
        literal += "/" + seg;
      }
    }
    if (literal) exprParts.push(`"${literal}"`);
    // Normalize: first literal must start with "/".
    if (exprParts.length > 0 && exprParts[0].startsWith(`"`) && !exprParts[0].startsWith(`"/`)) {
      exprParts[0] = `"/` + exprParts[0].slice(1);
    }
    const pathExpr = exprParts.join(" + ");

    const args: string[] = ["ctx context.Context"];
    for (const p of pathParams) args.push(`${goArgName(p)} string`);
    if (bodyType) args.push(`body ${bodyType}`);

    const summary = typeof op.summary === "string" ? op.summary : operationId;
    cl += `// ${goMethod} — ${summary}.\n`;
    cl += `// ${method.toUpperCase()} ${path}\n`;
    if (successType) {
      cl += `func (c *Client) ${goMethod}(${args.join(", ")}) (*${successType}, error) {\n`;
    } else {
      cl += `func (c *Client) ${goMethod}(${args.join(", ")}) error {\n`;
    }
    cl += `\tpath := ${pathExpr}\n`;
    let hooksExpr = "nil";
    if (errorHooks.length > 0) {
      hooksExpr = "map[int]func([]byte) error{\n";
      for (const h of errorHooks) {
        hooksExpr += `\t\t${h.status}: func(b []byte) error {\n`;
        hooksExpr += `\t\t\tvar e ${h.type}\n`;
        hooksExpr += `\t\t\tif err := json.Unmarshal(b, &e); err != nil {\n`;
        hooksExpr += `\t\t\t\treturn &APIError{StatusCode: ${h.status}, Body: b}\n\t\t\t}\n`;
        hooksExpr += `\t\t\treturn &e\n\t\t},\n`;
      }
      hooksExpr += "\t}";
    }
    const inExpr = bodyType ? "&body" : "nil";
    if (successType) {
      cl += `\tvar out ${successType}\n`;
      cl += `\tif err := c.do(ctx, "${method.toUpperCase()}", path, ${inExpr}, &out, ${hooksExpr}); err != nil {\n`;
      cl += `\t\treturn nil, err\n\t}\n`;
      cl += `\treturn &out, nil\n`;
    } else {
      cl += `\treturn c.do(ctx, "${method.toUpperCase()}", path, ${inExpr}, nil, ${hooksExpr})\n`;
    }
    cl += `}\n\n`;
  }

  cl += `func (c *Client) do(ctx context.Context, method, path string, in, out any, errHooks map[int]func([]byte) error) error {\n`;
  cl += `\tvar reqBody io.Reader\n`;
  cl += `\tif in != nil {\n\t\tbuf, err := json.Marshal(in)\n\t\tif err != nil {\n\t\t\treturn err\n\t\t}\n\t\treqBody = bytes.NewReader(buf)\n\t}\n`;
  cl += `\treq, err := http.NewRequestWithContext(ctx, method, strings.TrimRight(c.BaseURL, "/")+path, reqBody)\n`;
  cl += `\tif err != nil {\n\t\treturn err\n\t}\n`;
  cl += `\tif in != nil {\n\t\treq.Header.Set("Content-Type", "application/json")\n\t}\n`;
  cl += `\tif c.Token != "" {\n\t\treq.Header.Set("Authorization", "Bearer "+c.Token)\n\t}\n`;
  cl += `\thc := c.HTTPClient\n\tif hc == nil {\n\t\thc = http.DefaultClient\n\t}\n`;
  cl += `\tresp, err := hc.Do(req)\n\tif err != nil {\n\t\treturn err\n\t}\n`;
  cl += `\tdefer resp.Body.Close()\n`;
  cl += `\tbody, err := io.ReadAll(resp.Body)\n\tif err != nil {\n\t\treturn err\n\t}\n`;
  cl += `\tif resp.StatusCode >= 200 && resp.StatusCode < 300 {\n`;
  cl += `\t\tif out != nil && len(body) > 0 {\n\t\t\treturn json.Unmarshal(body, out)\n\t\t}\n\t\treturn nil\n\t}\n`;
  cl += `\tif hook, ok := errHooks[resp.StatusCode]; ok {\n\t\treturn hook(body)\n\t}\n`;
  cl += `\treturn &APIError{StatusCode: resp.StatusCode, Body: body}\n}\n`;
  writeFile(join(GO_CONTRACTS_DIR, cfg.goPackage, "client_gen.go"), cl);
}

